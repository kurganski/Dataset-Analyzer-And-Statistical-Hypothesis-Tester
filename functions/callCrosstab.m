function [tbl,chi2,p,labels] = callCrosstab( datasets )

% datasets = replaceNanStrings(datasets);

switch size(datasets,2)
    
    case 2
        [tbl,chi2,p,labels] = crosstab(...
            datasets(1).dataset,...
            datasets(2).dataset);
    case 3
        [tbl,chi2,p,labels] = crosstab(...
            datasets(1).dataset,...
            datasets(2).dataset, ...
            datasets(3).dataset);
    case 4
        [tbl,chi2,p,labels] = crosstab(...
            datasets(1).dataset,...
            datasets(2).dataset, ...
            datasets(3).dataset, ...
            datasets(4).dataset);
    case 5
        [tbl,chi2,p,labels] = crosstab(...
            datasets(1).dataset,...
            datasets(2).dataset, ...
            datasets(3).dataset,...
            datasets(4).dataset, ...
            datasets(5).dataset);
    case 6
        [tbl,chi2,p,labels] = crosstab(...
            datasets(1).dataset,...
            datasets(2).dataset, ...
            datasets(3).dataset,...
            datasets(4).dataset, ...
            datasets(5).dataset, ...
            datasets(6).dataset);
        
    otherwise
        assert(0, "Ќеверное количество выборок дл€ критери€");
end

end

